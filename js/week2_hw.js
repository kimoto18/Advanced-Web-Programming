// Declare two variables, a and b, and assign them different values
let a = 5;
let b = "5";

// Comparison using various operators
console.log("a == b:", a == b);       // loose equality, true because values are the same after coercion
console.log("a === b:", a === b);     // strict equality, false because types are different
console.log("a != b:", a != b);       // loose inequality, false because values are the same after coercion
console.log("a !== b:", a !== b);     // strict inequality, true because types are different
console.log("a > b:", a > b);         // greater than, false because 'b' is treated as a string
console.log("a < b:", a < b);         // less than, false because 'b' is treated as a string
console.log("a >= b:", a >= b);       // greater than or equal, true because 'b' is treated as a string
console.log("a <= b:", a <= b);       // less than or equal, true because 'b' is treated as a string






